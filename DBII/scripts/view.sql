CREATE OR REPLACE VIEW V_EMPLOYEES_FUNCTIONS AS 
SELECT FIRST_NAME || ' ' || LAST_NAME AS NOME_COMPLETO, JOB_TITLE FUNCAO, PHONE_NUMBER FONE, HIRE_DATE DT_CONTRATACAO
FROM HR.EMPLOYEES E INNER JOIN HR.JOBS J ON E.JOB_ID = J.JOB_ID;

SELECT * FROM V_EMPLOYEES_FUNCTIONS;    

