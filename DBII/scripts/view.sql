-- Listar o nome, o sobrenome, e a data de contratação dos funcionários que não são comissionados:
CREATE OR REPLACE VIEW V_UNCOMMISSIONED_EMPLOYEES AS 
SELECT FIRST_NAME, LAST_NAME, HIRE_DATE
FROM hr.EMPLOYEES
WHERE COMMISSION_PCT IS NULL;

SELECT * FROM V_UNCOMMISSIONED_EMPLOYEES;

